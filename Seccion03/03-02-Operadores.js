let a = 3, b = 2;
let z = a + b;

//Incremento
//Pre-incremento (El operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (El operador ++ despues de la variable)

z = b++;
console.log(b);
console.log(z);

//Decremento
//Pre-decremento
z = --a;
console.log(a);
console.log(z);

//Post-decremento
z = b--;
console.log(b);
console.log(z);
